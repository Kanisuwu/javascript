/*  Operadores Lógicos
 *  && -> AND -> E Todas as expressões precisam ser verdadeira para retornar verdadeiro.
 *  || -> OR -> OU
 *  ! -> NOT -> NÃO
 */

console.log(true && true); // True E true retornam true
console.log(false || true); // false OU true retorna true
console.log(!false); // Retorna true. Inverte valores